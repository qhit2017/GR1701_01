---学生表
CREATE table tb_student(
Sno char(11)primary key,
Sname char(20) not null,
Ssex char check (ssex='男'or ssex='女')default'男',
Sage smallint,
Sdept char(20))

---课程表
CREATE table tb_Course(
Cno char(4)primary key,
Cname char(20),
Cpno char(4),
Ccredit smallint)
---学生选课表
CREATE table tb_SC(
Sno char(11)primary key,
Cno char(4),
Grade smallint)

